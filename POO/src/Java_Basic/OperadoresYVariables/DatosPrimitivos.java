package Java_Basic.OperadoresYVariables;
public class DatosPrimitivos
{
    // Los tipos de datos es para asignar valores a nuestras variables
    // Asignar tipos de datos podemos realizar operaciones
    // Es para que se pueda compilar y se pueda interpretar en la memoria RAM

    // ********** D A T O S     P R I M I T I V O S **********

    public static void main(String[] args)
    {
        byte b = 127;
        short s = 5003;
        int i = 576498765;
        long l = 483940392;
        float f = 10.58f;
        double d = 278.756;
        boolean booleanTrue = true;
        boolean booleanFalse = false;
        char c = 'T';
        String miVariable = "Texto";


        System.out.println("Valor de byte: " + b);
        System.out.println("Valor de short: " + s);
        System.out.println("Valor de int: " + i);
        System.out.println("Valor de long: " + l);
        System.out.println("Valor de float: " + f);
        System.out.println("Valor de double: " + d);
        System.out.println("Valor de booleanTrue: " + booleanTrue);
        System.out.println("Valor de booleanFalse: " + booleanFalse);
        System.out.println("Valor de char: " + c);
        System.out.println("Valor de String: " + miVariable);

    }

}
